package ai.singlr.sail.gen;

import ai.singlr.sail.config.SailYaml;
import java.util.Map;

/**
 * Generates a self-documenting {@code sail.yaml} string with inline comments. Uses plain Java
 * string building (not SnakeYAML dump) because SnakeYAML Engine cannot emit comments. The output is
 * valid YAML that round-trips through {@code YamlUtil.parseMap()}.
 *
 * <p>Pure utility — no I/O, no shell execution.
 */
public final class SailYamlGenerator {

  private SailYamlGenerator() {}

  /**
   * Generates a commented sail.yaml string from the given configuration.
   *
   * @param config the project configuration
   * @return the complete YAML content as a string
   */
  public static String generate(SailYaml config) {
    var sb = new StringBuilder();

    sb.append(
        """
        # sail.yaml — Project descriptor for SAIL dev environments
        # Generated by `sail project init`. Edit freely.

        """);

    sb.append("# Project name — used as the Incus container name.\n");
    sb.append("# Must be lowercase alphanumeric with hyphens (e.g. my-project).\n");
    sb.append("name: ").append(config.name()).append("\n");

    if (config.description() != null) {
      sb.append("description: ").append(quoteYaml(config.description())).append("\n");
    }
    sb.append("\n");

    appendResources(sb, config.resources());

    sb.append("# Base container image. Default: ubuntu/24.04\n");
    sb.append("image: ")
        .append(quoteYaml(config.image() != null ? config.image() : "ubuntu/24.04"));
    sb.append("\n\n");

    appendPackages(sb, config);

    appendRuntimes(sb, config.runtimes());

    appendGit(sb, config.git());

    appendRepos(sb, config);

    appendServices(sb, config.services());

    appendSsh(sb, config.ssh());

    appendAgent(sb, config.agent());

    return sb.toString();
  }

  private static void appendResources(StringBuilder sb, SailYaml.Resources res) {
    sb.append(
        """
        # Resource limits for the Incus container.
        # CPU cores, memory, and disk are soft-allocated (dir) or hard (ZFS).
        resources:
        """);
    sb.append("  cpu: ").append(res.cpu()).append("\n");
    sb.append("  memory: ").append(res.memory()).append("\n");
    sb.append("  disk: ").append(res.disk()).append("\n\n");
  }

  private static void appendRuntimes(StringBuilder sb, SailYaml.Runtimes runtimes) {
    if (runtimes == null) return;
    if (runtimes.jdk() == 0 && runtimes.node() == null && runtimes.maven() == null) return;

    sb.append(
        """
        # Development runtimes installed inside the container.
        runtimes:
        """);
    if (runtimes.jdk() > 0) {
      sb.append("  jdk: ").append(runtimes.jdk()).append("\n");
    }
    if (runtimes.node() != null) {
      sb.append("  node: ").append(quoteYaml(runtimes.node())).append("\n");
    }
    if (runtimes.maven() != null) {
      sb.append("  maven: ").append(quoteYaml(runtimes.maven())).append("\n");
    }
    sb.append("\n");
  }

  private static void appendGit(StringBuilder sb, SailYaml.Git git) {
    if (git == null) return;

    sb.append(
        """
        # Git identity configured inside the container.
        # auth: 'token' (HTTPS + PAT) or 'ssh' (SSH key).
        # ssh_key: path to private key on host (only for auth: ssh).
        git:
        """);
    sb.append("  name: ").append(quoteYaml(git.name())).append("\n");
    sb.append("  email: ").append(quoteYaml(git.email())).append("\n");
    sb.append("  auth: ").append(git.auth()).append("\n");
    if (git.sshKey() != null) {
      sb.append("  ssh_key: ").append(quoteYaml(git.sshKey())).append("\n");
    }
    sb.append("\n");
  }

  private static void appendRepos(StringBuilder sb, SailYaml config) {
    if (config.repos() == null || config.repos().isEmpty()) return;

    sb.append(
        """
        # Repositories to clone into ~/workspace/ during project creation.
        repos:
        """);
    for (var repo : config.repos()) {
      sb.append("  - url: ").append(quoteYaml(repo.url())).append("\n");
      sb.append("    path: ").append(quoteYaml(repo.path())).append("\n");
      if (repo.branch() != null) {
        sb.append("    branch: ").append(repo.branch()).append("\n");
      }
    }
    sb.append("\n");
  }

  private static void appendPackages(StringBuilder sb, SailYaml config) {
    if (config.packages() == null || config.packages().isEmpty()) return;

    sb.append("# Additional Ubuntu packages to install.\n");
    sb.append("packages:\n");
    for (var pkg : config.packages()) {
      sb.append("  - ").append(quoteYaml(pkg)).append("\n");
    }
    sb.append("\n");
  }

  private static void appendServices(StringBuilder sb, Map<String, SailYaml.Service> services) {
    if (services == null || services.isEmpty()) return;

    sb.append(
        """
        # Infrastructure services run as rootless Podman containers.
        # Each service gets --restart=always and survives reboots via linger.
        services:
        """);
    for (var entry : services.entrySet()) {
      var name = entry.getKey();
      var svc = entry.getValue();
      sb.append("  ").append(name).append(":\n");
      sb.append("    image: ").append(quoteYaml(svc.image())).append("\n");
      if (svc.ports() != null && !svc.ports().isEmpty()) {
        sb.append("    ports: [");
        for (var i = 0; i < svc.ports().size(); i++) {
          if (i > 0) sb.append(", ");
          sb.append(svc.ports().get(i));
        }
        sb.append("]\n");
      }
      if (svc.environment() != null && !svc.environment().isEmpty()) {
        sb.append("    environment:\n");
        for (var env : svc.environment().entrySet()) {
          sb.append("      ")
              .append(env.getKey())
              .append(": ")
              .append(quoteYaml(env.getValue()))
              .append("\n");
        }
      }
      if (svc.volumes() != null && !svc.volumes().isEmpty()) {
        sb.append("    volumes:\n");
        for (var vol : svc.volumes()) {
          sb.append("      - ").append(quoteYaml(vol)).append("\n");
        }
      }
      if (svc.command() != null) {
        sb.append("    command: ").append(quoteYaml(svc.command())).append("\n");
      }
    }
    sb.append("\n");
  }

  private static void appendSsh(StringBuilder sb, SailYaml.Ssh ssh) {
    if (ssh == null) return;

    sb.append(
        """
        # SSH access configuration. The user is created inside the container.
        # Add your public key(s) for Zed remote dev access.
        ssh:
        """);
    sb.append("  user: ").append(ssh.user()).append("\n");
    if (ssh.authorizedKeys() != null && !ssh.authorizedKeys().isEmpty()) {
      sb.append("  authorized_keys:\n");
      for (var key : ssh.authorizedKeys()) {
        sb.append("    - ").append(quoteYaml(key)).append("\n");
      }
    }
    sb.append("\n");
  }

  private static void appendAgent(StringBuilder sb, SailYaml.Agent agent) {
    if (agent == null) return;

    sb.append(
        """
        # AI coding agent configuration.
        # type: primary agent for `sail agent start` (claude-code | codex | helios)
        # install: agent CLIs to install during provisioning (defaults to [type] if omitted)
        agent:
        """);
    sb.append("  type: ").append(agent.type()).append("\n");
    sb.append("  auto_snapshot: ").append(agent.autoSnapshot()).append("\n");
    sb.append("  auto_branch: ").append(agent.autoBranch()).append("\n");
    if (agent.branchPrefix() != null) {
      sb.append("  branch_prefix: ").append(quoteYaml(agent.branchPrefix())).append("\n");
    }
    if (agent.install() != null && !agent.install().isEmpty()) {
      sb.append("  install:\n");
      for (var cli : agent.install()) {
        sb.append("    - ").append(cli).append("\n");
      }
    }
    if (agent.specsDir() != null) {
      sb.append("  specs_dir: ").append(quoteYaml(agent.specsDir())).append("\n");
    }
    if (agent.guardrails() != null) {
      sb.append("  guardrails:\n");
      var g = agent.guardrails();
      if (g.maxDuration() != null) {
        sb.append("    max_duration: ").append(g.maxDuration()).append("\n");
      }
      sb.append("    action: ").append(g.action()).append("\n");
    }
    if (agent.methodology() != null) {
      sb.append("  methodology:\n");
      var m = agent.methodology();
      if (m.approach() != null) {
        sb.append("    approach: ").append(m.approach()).append("\n");
      }
      if (m.verify() != null) {
        sb.append("    verify: ").append(quoteYaml(m.verify())).append("\n");
      }
      if (m.lint() != null) {
        sb.append("    lint: ").append(quoteYaml(m.lint())).append("\n");
      }
    }
    sb.append("\n");
  }

  /** Quotes a YAML string value if it contains special characters. */
  static String quoteYaml(String value) {
    if (value == null) return "\"\"";
    if (value.isEmpty()) return "\"\"";
    if (value.contains(":")
        || value.contains("#")
        || value.contains("\"")
        || value.contains("'")
        || value.contains("{")
        || value.contains("}")
        || value.contains("[")
        || value.contains("]")
        || value.contains(",")
        || value.contains("&")
        || value.contains("*")
        || value.contains("!")
        || value.contains("|")
        || value.contains(">")
        || value.contains("@")
        || value.contains("`")
        || value.contains(" ")
        || value.contains("\n")
        || value.contains("\t")
        || value.startsWith("-")) {
      return "\""
          + value
              .replace("\\", "\\\\")
              .replace("\"", "\\\"")
              .replace("\n", "\\n")
              .replace("\t", "\\t")
          + "\"";
    }
    return value;
  }
}
