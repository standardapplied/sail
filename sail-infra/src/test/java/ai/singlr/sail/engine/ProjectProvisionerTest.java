  @Test
  void launchFailureRecordsErrorInTracker() {
    var shell =
        new ScriptedShellExecutor()
            .onFail("incus info acme-health", "not found")
            .onFail("incus launch", "permission denied");
    var tracker = tracker();

    var provisioner = new ProjectProvisioner(shell, tracker, null);

    var ex =
        assertThrows(
            IOException.class, () -> provisioner.provision(fullConfig(), hostYaml(), null, null));
    assertTrue(ex.getMessage().contains("Failed to launch container"));

    var state = tracker.currentState();
    assertNotNull(state.error());
    assertEquals("CONTAINER_LAUNCHED", state.error().failedPhase());
    assertTrue(state.error().message().contains("permission denied"));
  }

