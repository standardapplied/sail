  @Test
  void commandOrderingIsCorrect() throws Exception {
    var shell = allSuccessShell();
    var provisioner = new ProjectProvisioner(shell, tracker(), null);

    provisioner.provision(fullConfig(), hostYaml(), null, null);

    var cmds = shell.invocations();
    var launch = indexOfContaining(cmds, "incus launch");
    var diskSet = indexOfContaining(cmds, "incus config device override");
    var limitsSet = indexOfContaining(cmds, "incus config set");
    var aptUpdate = indexOfContaining(cmds, "apt-get update");
    var aptInstall = indexOfContaining(cmds, "apt-get install");
    var sshEnable = indexOfContaining(cmds, "systemctl enable --now ssh");
    var loginctlLinger = indexOfContaining(cmds, "loginctl enable-linger");
    var podmanRun = indexOfContaining(cmds, "podman run");

    assertTrue(launch < diskSet, "launch before disk quota");
    assertTrue(diskSet < limitsSet, "disk before limits");
    assertTrue(limitsSet < aptUpdate, "limits before apt-get");
    assertTrue(aptUpdate < aptInstall, "update before install");
    assertTrue(aptInstall < sshEnable, "install before ssh");
    assertTrue(sshEnable < loginctlLinger, "ssh before podman setup");
    assertTrue(loginctlLinger < podmanRun, "linger before podman run");
  }

  private static int indexOfContaining(List<String> list, String substring) {
    for (var i = 0; i < list.size(); i++) {
      if (list.get(i).contains(substring)) {
        return i;
      }
    }
    return -1;
  }

